// 通过串口修改K矩阵，实现实时调lqr
int8_t Chassis_Get_K(GetLQR_K_t *rx_data)
{
    if (rx_data->data[0] != 0xCD || rx_data->data[49] != 0xDC)
    {
        return -1;
    }
    memcpy(ChassisL_LQR_K, rx_data->K_data.LQR_K, sizeof(float) * 12);
    return 0;
}